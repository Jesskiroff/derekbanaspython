import math


print("ceil(4.4)= ",  math.ceil(4.4)) #5

print("floor(4.4)= ", math.floor(4.4)) #4

print("fabs(-4.4)= ", math.fabs(-4.4)) #4.4

#factorial = 1 * 2 * 3 * 4
print("factorial(4)", math.factorial(4)) #24

print('fmod()= ', math.fmod(5,4)) #1.0

print('trunc(4.2)= ', math.trunc(4.2)) #4
print("pow(2,2) = ", math.pow(2,2)) #4.0